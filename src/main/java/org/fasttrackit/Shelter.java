package org.fasttrackit;

public class Shelter {
   private String name;
   private int animalsCapacity;
   private int cleanProcentage;
   private int foodCapacity;
   private int waterCapacity;
   private int foodProcentage;
   private int waterProcentage;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAnimalsCapacity() {
        return animalsCapacity;
    }

    public void setAnimalsCapacity(int animalsCapacity) {
        this.animalsCapacity = animalsCapacity;
    }

    public int getCleanProcentage() {
        return cleanProcentage;
    }

    public void setCleanProcentage(int cleanProcentage) {
        this.cleanProcentage = cleanProcentage;
    }

    public int getFoodCapacity() {
        return foodCapacity;
    }

    public void setFoodCapacity(int foodCapacity) {
        this.foodCapacity = foodCapacity;
    }

    public int getWaterCapacity() {
        return waterCapacity;
    }

    public void setWaterCapacity(int waterCapacity) {
        this.waterCapacity = waterCapacity;
    }

    public int getFoodProcentage() {
        return foodProcentage;
    }

    public void setFoodProcentage(int foodProcentage) {
        this.foodProcentage = foodProcentage;
    }

    public int getWaterProcentage() {
        return waterProcentage;
    }

    public void setWaterProcentage(int waterProcentage) {
        this.waterProcentage = waterProcentage;
    }
}
